void ov2640_power_on(void)
{
    if (OV2640_PIN_PWDN >= 0) {
         rt_pin_write(OV2640_PIN_PWDN, PIN_LOW);
    }
}

void ov2640_hw_reset(void)
{
    rt_pin_write(OV2640_PIN_RESET, PIN_LOW);
    rt_thread_mdelay(10);
    rt_pin_write(OV2640_PIN_RESET, PIN_HIGH);
    rt_thread_mdelay(100);
}

void ov2640_sw_reset(void)
{
    ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 0xFF, 0x01); // 映射表DLMT
    ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 0x12, 0x80); // COM7
    rt_thread_mdelay(100);
}

static uint8_t ov2640_read_id(void)
{
    uint16_t reg;
    reg = ov2640.i2c.read_byte(&ov2640.i2c, OV2640_ADDR, 0x1C); // SENSOR_MIDH
	reg <<= 8;
	reg |= ov2640.i2c.read_byte(&ov2640.i2c, OV2640_ADDR, 0x1D); // SENSOR_MIDL
    if (reg != OV2640_MID) return 1;
    
    reg = ov2640.i2c.read_byte(&ov2640.i2c, OV2640_ADDR, 0x0A); // SENSOR_PIDH
	reg <<= 8;
	reg |= ov2640.i2c.read_byte(&ov2640.i2c, OV2640_ADDR, 0x0B); // SENSOR_PIDL
    if (reg != OV2640_PID) return 1;
    
    return 0;
}

