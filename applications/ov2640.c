static int32_t get_jpg_data(uint8_t *jpg_data, uint32_t len)
{
    uint32_t jpeglen = 0;
    uint8_t head = 0, tail = 0;
    uint32_t timeout;
    rt_base_t level;

    // 1. 同步新帧：先等VSYNC为低
    timeout = 0x1FFFFFF;
    while(OV2640_VSYNC == 1) { if(--timeout == 0) return 0; }
    
    // 2. 等VSYNC为高，意味着全新一帧开始
    timeout = 0x1FFFFFF;
    while(OV2640_VSYNC == 0) { if(--timeout == 0) return 0; }

    // **********************************************
    // 致命武器：关闭 RT-Thread 调度，变成纯裸机！
    // **********************************************
    level = rt_hw_interrupt_disable();

    while (OV2640_VSYNC == 1)
    {
        while (OV2640_HREF == 1)
        {
            while (OV2640_PCLK == 0) {} 
            
            // 安全存入数据
            if (jpeglen < len) {
                jpg_data[jpeglen] = OV2640_DATA;
            } else {
                rt_hw_interrupt_enable(level); // 越界退出前开中断
                return 0; 
            }
            
            while (OV2640_PCLK == 1) {}
            
            // 帧解析状态机
            if (!head) {
                if (jpeglen == 0) {
                    if (jpg_data[jpeglen] == 0xff) jpeglen++;
                } else if (jpeglen > 0) {
                    if (jpg_data[jpeglen] == 0xd8 && jpg_data[jpeglen-1] == 0xff) {
                        head = 1; jpeglen++;
                    } else {
                        jpeglen--; 
                    }
                }
            } else if (!tail) {
                if (jpg_data[jpeglen] == 0xd9 && jpg_data[jpeglen-1] == 0xff) {
                    tail = 1;
                    jpeglen++;
                    
                    // 抓图完毕，恢复 RT-Thread 调度
                    rt_hw_interrupt_enable(level);
                    return jpeglen; 
                } else {
                    jpeglen++;
                }
            }
        }
    }
    
    rt_hw_interrupt_enable(level); // 异常退出时恢复中断
    return 0;
}

static void set_outsize(IMAGE_FORMAT image_format)
{
    uint32_t i;
    switch(image_format)
    {
        case JPEG_160x120:
            for(i=0; i<(sizeof(OV2640_160x120_JPEG)/2); i++)
                ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, OV2640_160x120_JPEG[i][0], OV2640_160x120_JPEG[i][1]);
            break;
        // ... (保留其它分辨率配置)
        default:
            for(i=0; i<(sizeof(OV2640_160x120_JPEG)/2); i++)
                ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, OV2640_160x120_JPEG[i][0], OV2640_160x120_JPEG[i][1]);
    }
}

static void set_speed(uint8_t clkdiv, uint8_t pclkdiv)
{
    ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 0XFF, 0X00);
	ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 0XD3, pclkdiv);
	ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 0XFF, 0X01);
	ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 0X11, clkdiv);
}

static void ov2640_gpio_init()
{
    // 配置复位和低功耗控制脚
    rt_pin_mode(OV2640_PIN_RESET, PIN_MODE_OUTPUT);
    if (OV2640_PIN_PWDN >= 0) {
        rt_pin_mode(OV2640_PIN_PWDN, PIN_MODE_OUTPUT);
    }
    
    // 【重要】数据引脚交由 STM32 底层自动配置，或确认 BSP 已经初始化过输入浮空/上拉。
    // 在 RT-Thread 中大量调用 rt_pin_mode 也是安全的，由于采用寄存器直读，后续不受影响。
}

static uint8_t ov2640_init(IMAGE_FORMAT format)
{
    int32_t i;
    uint8_t ret = 0;
    
    ov2640_gpio_init();
    ov2640.power_on();
    ov2640.hw_reset();
    
    i2c_init(&ov2640.i2c, OV2640_PIN_SCL, OV2640_PIN_SDA);
    ov2640.i2c.set_i2c_sccb(&ov2640.i2c, 1);
    
    rt_thread_mdelay(100);
    ov2640.sw_reset();
    
    ret = ov2640.read_id();
    if (ret) return ret;
    
	for(i=0; i<sizeof(ov2640_svga_init_reg_tbl)/2; i++) {
	   	ov2640.i2c.write_byte(&ov2640.i2c, OV2640_ADDR, 
            ov2640_svga_init_reg_tbl[i][0], ov2640_svga_init_reg_tbl[i][1]);
 	}
    
    ov2640.set_jpeg_mode();
    ov2640.set_outsize(format);
    ov2640.set_speed(4, 32); 
    
    return ret;
}

